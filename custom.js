$('#morse').on('keyup', function () {
  var value  = $(this).val();
  var output = '';

  if (value === '') {
    $('#text').text('');
    
    return false;
  } // if (value === '')

  for (var i = 0; i < value.length; i++) {
    output += converter[value.charAt(i).toLowerCase()];
  } // for (var i = 0; i < value.length; i++)
  
  $('#text').text(output);
});

